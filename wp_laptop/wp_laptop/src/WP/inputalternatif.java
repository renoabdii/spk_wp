    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
                // TODO add your handling code here:
          // Ambil nilai dari input fields dan combo box
    // Mengambil nilai dari input fields

String namaAlternatif = txtnama.getText();
String hargaAlternatif = txtharga.getText();
String ramAlternatif = txtram.getText();
String penyimpananAlternatif = txtpenyimpanan.getText();
String gpuAlternatif = txtgpu.getText();
String layarAlternatif = txtlayar.getText();
String bateraiAlternatif = txtbaterai.getText();

// Pastikan nilai tidak kosong
if (!namaAlternatif.isEmpty() && !hargaAlternatif.isEmpty() && !ramAlternatif.isEmpty() && !penyimpananAlternatif.isEmpty() && !gpuAlternatif.isEmpty()&& !layarAlternatif.isEmpty()&& !bateraiAlternatif.isEmpty()) {
    // Koneksi ke database
    Connection kon = null;
    PreparedStatement pstmt = null;
    
    try {
        // Dapatkan koneksi dari kelas Koneksi
        kon = koneksi.koneksiDB();
        
        // Query untuk memasukkan data
        String sql = "INSERT INTO alternatif (nama_laptop, harga, ram, penyimpanan, gpu, layar, baterai) VALUES (?, ?, ?, ?, ?, ?, ?)";
        pstmt = kon.prepareStatement(sql);
        pstmt.setString(1, namaAlternatif);
        pstmt.setString(2, hargaAlternatif);
        pstmt.setString(3, ramAlternatif);
        pstmt.setString(4, penyimpananAlternatif);
        pstmt.setString(5, gpuAlternatif);
        pstmt.setString(6, layarAlternatif);
        pstmt.setString(7, bateraiAlternatif);
       
        // Eksekusi query
        pstmt.executeUpdate();
        
        // Tampilkan pesan sukses
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        
        // Kosongkan input fields setelah data disimpan
        
        txtnama.setText("");
        txtharga.setText("");
        txtram.setText("");
        txtpenyimpanan.setText("");
        txtgpu.setText("");
        txtlayar.setText("");
        txtbaterai.setText("");
        tampilData();
    } catch (Exception e) {
        // Tampilkan pesan kesalahan jika terjadi kesalahan
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } 
} else {
    // Tampilkan pesan kesalahan jika ada nilai yang kosong
    JOptionPane.showMessageDialog(this, "Semua field harus diisi.", "Error", JOptionPane.ERROR_MESSAGE);
}



    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        // Ambil nilai dari input fields dan combo box
    String nama = txtnama.getText();
    String harga = txtharga.getText();
    String ram = txtram.getText();
    String penyimpanan = txtpenyimpanan.getText();
    String gpu = txtgpu.getText();
    String layar = txtlayar.getText();
    String baterai = txtlayar.getText();

    // Dapatkan baris yang dipilih dari tabel
    int selectedRow = tblAlternatif.getSelectedRow();
    if (selectedRow != -1) {
        // Dapatkan id dari baris yang dipilih
        int id = Integer.parseInt(tblAlternatif.getValueAt(selectedRow, 0).toString());

        if (!nama.isEmpty() && !harga.isEmpty() && !ram.isEmpty() && !penyimpanan.isEmpty() && !gpu.isEmpty() && !layar.isEmpty() && !baterai.isEmpty()) {
            try {
                // Update data di database
                Connection kon = koneksi.koneksiDB();
                String sql = "UPDATE alternatif SET nama = ?, harga = ?, ram = ?, penyimpanan = ?, gpu = ?, layar = ?, baterai = ? WHERE id = ?";
                PreparedStatement pst = kon.prepareStatement(sql);
                pst.setString(1, nama);
                pst.setString(2, harga);
                pst.setString(3, ram);
                pst.setString(4, penyimpanan);
                pst.setString(5, gpu);
                pst.setString(6, layar);
                pst.setString(7,baterai);
                pst.setInt(8, id);
                pst.executeUpdate();

                // Perbarui tabel tampilan setelah data diupdate
                DefaultTableModel model = (DefaultTableModel) tblAlternatif.getModel();
                model.setValueAt(nama, selectedRow, 1);
                model.setValueAt(harga, selectedRow, 2);
                model.setValueAt(ram, selectedRow, 3);
                model.setValueAt(penyimpanan, selectedRow, 4);
                model.setValueAt(gpu, selectedRow, 5);
                model.setValueAt(layar, selectedRow, 6);
                model.setValueAt(baterai, selectedRow, 7);
                
                

//                 Kosongkan input fields setelah data disimpan
                txtnama.setText("");
                txtharga.setText("");
                txtram.setText("");
                txtpenyimpanan.setText("");
                txtgpu.setText("");
                txtlayar.setText("");
                txtbaterai.setText("");
                
                tampilData();

                JOptionPane.showMessageDialog(this, "Data berhasil diupdate.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Harap isi semua field sebelum menyimpan data.", "Input Kosong", JOptionPane.WARNING_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Harap pilih data yang ingin diupdate dari tabel.", "Tidak Ada Data Terpilih", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btneditActionPerformed

