    private void hapusdata(){
        
    int row = tblAlternatif.getSelectedRow();
    
    if (row >= 0) {
        DefaultTableModel model = (DefaultTableModel) tblAlternatif.getModel();
        String id = model.getValueAt(row, 0).toString();  // Ambil ID dari baris yang dipilih
        
        // Hapus dari database\
        Connection kon = null;
        PreparedStatement pstmt = null;
    
        try {
        // Dapatkan koneksi dari kelas Koneksi
        kon = koneksi.koneksiDB();
        
            String query = "DELETE FROM alternatif WHERE id=?";
            PreparedStatement ps = kon.prepareStatement(query);
            ps.setString(1, id);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus dari database");

            // Hapus dari tabel
            model.removeRow(row);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dihapus dari database: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(rootPane, "Pilih baris yang ingin dihapus");
    }
}

    
    public void tampilData() {
    DefaultTableModel tbl = new DefaultTableModel();
    tbl.addColumn("No");
    tbl.addColumn("Nama");
    tbl.addColumn("Harga");
    tbl.addColumn("ram");
    tbl.addColumn("penyimpanan");
    tbl.addColumn("gpu");
    tbl.addColumn("layar");
    tbl.addColumn("baterai");
    
   
    try {
        Connection conn = koneksi.koneksiDB();
        String sql = "SELECT * FROM alternatif";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        int no = 1;
        while (rs.next()) {
            tbl.addRow(new Object[]{ 
                no++,
                rs.getString(2),
                rs.getString(3),
                rs.getString(4),
                rs.getString(5),
                rs.getString(6),
                rs.getString(7),
                rs.getString(8),
            });
        }

        tblAlternatif.setModel(tbl);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menampilkan data: " + e.getMessage());
        e.printStackTrace();
    }
}
